function setLanguage(lang) {
    if (!translations[lang]) lang = 'en';
    localStorage.setItem('lang', lang);
    translatePage(lang);
    updateLangButtons(lang);
}

function updateLangButtons(activeLang) {
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.setAttribute('aria-pressed', btn.getAttribute('data-lang') === activeLang ? 'true' : 'false');
    });
}

function translatePage(lang) {
    document.querySelectorAll('[data-lang-key]').forEach(el => {
        const key = el.getAttribute('data-lang-key');
        if (translations[lang][key]) {
            if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
                el.placeholder = translations[lang][key];
            } else {
                el.innerText = translations[lang][key];
            }
        }
    });

    const titleEl = document.querySelector('title[data-lang-key]');
    if (titleEl) {
        const key = titleEl.getAttribute('data-lang-key');
        document.title = translations[lang][key];
    }
}

function renderServices(lang) {
    const container = document.querySelector('.services-grid');
    if (!container) return;

    container.querySelectorAll('.service-card').forEach(el => el.remove());

    servicesData.forEach(service => {
        const card = document.createElement('div');
        card.className = 'service-card';
        card.innerHTML = `
            <img src="${service.img}" alt="${translations[lang][service.titleKey]}">
            <h3 data-lang-key="${service.titleKey}">${translations[lang][service.titleKey]}</h3>
            <p data-lang-key="${service.textKey}">${translations[lang][service.textKey]}</p>
        `;
        container.appendChild(card);
    });
}

function renderEvents(lang) {
    const container = document.querySelector('.events-grid');
    if (!container) return;

    container.querySelectorAll('.event-card').forEach(el => el.remove());

    eventsData.forEach(event => {
        const card = document.createElement('article');
        card.className = 'event-card';
        card.innerHTML = `
            <img src="${event.img}" alt="${translations[lang][event.titleKey]}">
            <h3 data-lang-key="${event.titleKey}">${translations[lang][event.titleKey]}</h3>
            <p data-lang-key="${event.dateKey}">${translations[lang][event.dateKey]}</p>
            <p data-lang-key="${event.locationKey}">${translations[lang][event.locationKey]}</p>
        `;
        container.appendChild(card);
    });
}

function renderFooter() {
    const footer = document.querySelector('footer');
    if (footer) {
        footer.innerText = "© 2025 Ireme Events Rwanda. All rights reserved.";
    }
}

const businessNumber = "250785053518";

function initContactForm() {
    const form = document.getElementById('contactForm');
    if (!form) return;

    form.addEventListener('submit', (e) => {
        e.preventDefault();
        const name = document.getElementById('name').value.trim();
        const email = document.getElementById('email').value.trim();
        const message = document.getElementById('message').value.trim();

        if(!name || !email || !message){
            alert("Please fill all fields.");
            return;
        }

        const whatsappMsg = encodeURIComponent(
            `📩 New Contact Message\n\n👤 Name: ${name}\n📧 Email: ${email}\n💬 Message: ${message}`
        );

        window.open(`https://wa.me/${businessNumber}?text=${whatsappMsg}`, "_blank");
        form.reset();
    });
}

function initTranslations() {
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => {
            const lang = btn.getAttribute('data-lang');
            setLanguage(lang);
            renderServices(lang);
            renderEvents(lang);
        });
    });

    const savedLang = localStorage.getItem('lang');
    const initialLang = savedLang && translations[savedLang] ? savedLang : 'en';

    setLanguage(initialLang);
    renderServices(initialLang);
    renderEvents(initialLang);
    renderFooter();
    initContactForm();
}

if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initTranslations);
} else {
    initTranslations();
}
